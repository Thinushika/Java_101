public class L5_ExpressionsStatementsBlocks {
    public static void main(String[] args) {
        // block - group of statements (zero or more) that enclose by {}
        String band = "Beatles";
        if (band == "Beatles"){
            // start of block
            System.out.print("Hey ");
            System.out.print("Jude!");
        } // end of block
    }
}

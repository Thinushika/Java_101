    private void addbookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addbookMouseClicked
        // TODO add your handling code here:
        String sql="Insert into books(id,title,author,isbn,publisher,category,rackno,price)values(?,?,?,?,?,?,?,?)";
        try {

            pst=conn.prepareStatement(sql);
            pst.setString(1,txt_bookID.getText());
            pst.setString(2,txt_booktitle.getText());
            pst.setString(3,txt_Author.getText());
            pst.setString(4,txt_isbn.getText());
            pst.setString(5,txt_Publisher.getText());
            pst.setString(6,(String)jComboBox_category.getSelectedItem());
            pst.setString(7,(String)jComboBox_RackNo.getSelectedItem());
            pst.setString(8,txt_Price.getText());
            
            pst.execute();
            JOptionPane.showMessageDialog(null,"New Book Added");
            rs.close();
            pst.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }//GEN-LAST:event_addbookMouseClicked


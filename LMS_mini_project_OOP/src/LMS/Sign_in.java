    private void btn_LogInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_LogInMouseClicked
        // TODO add your handling code here:
        String sql = "select * from accounts where User_name=?and Password=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1,txt_username.getText());
            pst.setString(2,jPasswordField1.getText());
            rs=pst.executeQuery();
            if (rs.next()){
                rs.close();
                pst.close();

                this.dispose();
                Loading L = new Loading();
                L.setUpLoading();
                L.setVisible(true);
                
            }
            else{
                txt_error.setText("Please check your inputs ");
                txt_error.setForeground(new Color(255,255,255));
                txt_error.setBackground(new Color(255,0,0));
                txt_error.setVisible(true);
                cheking.setBackground(new Color(255,0,0));
                cheking.setVisible(true);
                lbl_error.setVisible(true);
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception ex){

            }//102,102,102
        }

    }//GEN-LAST:event_btn_LogInMouseClicked

